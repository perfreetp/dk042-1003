export const getNotificationsByRecallId = (recallTaskId: string): Notification[] => {
  return mockNotifications.filter((n) => n.recallTaskId === recallTaskId);
};

export const getNotificationsByRecipient = (recipientId: string): Notification[] => {
  return mockNotifications.filter((n) => n.recipientId === recipientId);
};

export const getNotificationById = (id: string): Notification | undefined => {
  return mockNotifications.find((n) => n.id === id);
};

export const getOverdueNotifications = (): Notification[] => {
  return mockNotifications.filter((n) => n.isOverdue);
};

export const getUnreadNotifications = (recipientId?: string): Notification[] => {
  if (recipientId) {
    return mockNotifications.filter((n) => n.recipientId === recipientId && n.status === 'unread');
  }
  return mockNotifications.filter((n) => n.status === 'unread');
};
